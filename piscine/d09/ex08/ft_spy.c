#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int		ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	while (argv[i])
	{
		if (ft_strcmp(argv[i], "president") == 0 ||
			ft_strcmp(argv[i], "attack") == 0 ||
			ft_strcmp(argv[i], "Bauer") == 0)
		{
			ft_putstr("Alert!!!");
			return (0);
		}
		i++;
	}
	return (0);
}
