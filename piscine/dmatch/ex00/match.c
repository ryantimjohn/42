int	match(char *s1, char *s2)
{
	while (*s2)
	{
		if (*s2 == '*')
		{
			if (match(s1, s2 + 1))
				return (1);
			if (*s1 != 0 && match(s1 + 1, s2))
				return (1);
			return (0);
		}
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
			return (0);
	}
	if (*s1 == *s2)
		return (1);
	return (0);
}
