int	ft_iterative_factorial(int nb)
{
	int n;
	int i;

	i = 0;
	n = 1;
	if (nb == 0)
	{
		return (1);
	}
	if (nb > 12 || nb < -12)
	{
		return (0);
	}
	if (nb > 0)
	{
		while (nb > i)
		{
			n *= (nb - i);
			i++;
		}
		return (n);
	}
	return (0);
}
