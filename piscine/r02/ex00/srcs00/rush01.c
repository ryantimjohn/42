#include "rush_2.h"

void	rush(int x, int y)
{
	int	i;
	int	j;

	j = 1;
	while (j <= y)
	{
		i = 1;
		while (i <= x)
		{
			if (i * j == 1 || (i * j == x * y && i != 1 && j != 1))
				ft_putchar('/');
			else if ((i * 1 == x && j == 1) || (1 * j == y && i == 1))
				ft_putchar('\\');
			else if (i == 1 || i == x || j == 1 || j == y)
				ft_putchar('*');
			else
				ft_putchar(' ');
			i++;
		}
		ft_putchar('\n');
		j++;
	}
}
