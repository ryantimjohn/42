#include "ft_eval_expr.h"

int	ft_atoi(char **a)
{
	int i;
	int posneg;

	i = 0;
	posneg = 1;
	while (**a && ((**a >= '\t' && **a <= '\r') || **a == ' '))
		(*a)++;
	if (**a == '+' || **a == '-')
	{
		if (**a == '-')
			posneg = -posneg;
		(*a)++;
	}
	while (**a >= '0' && **a <= '9')
	{
		i = (i * 10) + (**a - '0');
		(*a)++;
	}
	return (i * posneg);
}
